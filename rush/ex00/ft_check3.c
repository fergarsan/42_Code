int ft_check3(int *n)
{
	int t;
	int i;
	int j;

	i = 0;
	j = 0;
	t = 0;
	while (j < 4)
	{
		while (i < 4)
		{
			if (!(n[i][j] > 0 && n[i][j]< 5))
			{
				t == 1;
			}
			j++;
		}
		j = 0;
		i ++;
	}
	return (t);
}
